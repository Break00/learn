package com.jason.lee.offer;

/**
 * 顶点
 */
public class Vertex {
    /**
     * 顶点序号
     */
    public int index;
    /**
     * 边
     */
    public Edge edge;


    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public Edge getEdge() {
        return edge;
    }

    public void setEdge(Edge edge) {
        this.edge = edge;
    }
}
